  <h2 id="how-we-secure-your-information">How we secure your information</h2>
  <p>
    We believe in a defense-in-depth strategy that leverages multiple layers of security to provide the best level of
    protection available. We utilize a web-application firewall that inspects all traffic for malicious intent as well
    as blocks known bad actors and emerging attacks. We use strict firewall rules that limit access to only traffic that
    has gone through the web-application firewall. Our servers are managed in what is called a &quot;virtual private
    cloud&quot; which means they exist on a private network where no other traffic is able to reach them. Further, your
    information stored in our database is encrypted both in transit and at rest. Finally, we operate with a least-access
    privilege model which restricts access to servers and services to only the individuals or other services which need
    access to them. All access into and out of the network is logged as well as administrative access to servers and
    services. We even remove location (GPS) information from any photos you choose to upload to Cover.
